def fun(n, a):
    # **********Program**********
    s = 0
    t = 0
    for j in range(n):
        t += a * 10 ** j
    s += t
    return s

    # **********  End  **********


def main():
    print("【请连续三次计算公式的值：】")
    for n in range(3):
        print("【第%d次：】" % (n + 1))
        a = int(input("【请输入 1-10 之间的数字a：】"))
        n = int(input("【请输入数字的个数n：】"))
        sum = 0
        for i in range(1, n + 1):
            sum = sum + fun(i, a)
        print("【sum=】", sum)


if __name__ == '__main__':
    main()
