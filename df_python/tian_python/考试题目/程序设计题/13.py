'''------------------------------------------------------
【程序设计】
---------------------------------------------------------

题目：奇数数列求和

描述：输入一个正整数 n,求奇数数列：1，3，5，……，（2n-1）的和。

要求：

     输入格式：一个正整数 n


     输出格式：数列的和



      输入示例    输出示例

 示例1   3           9
---------------------------------------------------------
注意：仅在注释标志之间填入所编写语句。
------------------------------------------------------'''

# **********Program**********
n = int(input("请输入一个正整数："))
s = 0
for i in range(1, n + 1):
    s += 2 * i - 1
print(n, "\t", s)

