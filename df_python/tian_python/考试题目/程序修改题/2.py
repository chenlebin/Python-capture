def main():
    l = [1, 2, 3, 4, 5, 6, 7]
    # **********FOUND**********
    k = 1
    # **********FOUND**********
    for i in l:
        print(i, end=('' if (k == len(l)) else ','))
        # **********FOUND**********
        k = k + 1


if __name__ == '__main__':
    main()
